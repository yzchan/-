; ================================================
; 功能：比对两段内存中数据是否相同
; 参数：
; 	ds:si 指向源地址
; 	es:di 指向目标地址
; 	cx    比较次数
; 出参：
; 	ZF=1表示相等  ZF=0表示不等
; ------------------------------------------------
; author: chenyuzou@qq.com
; date: 2022-05-01
; ================================================

CmpMem:
	push si
	push di
.loop:
	cmpsb
	jnz .end
	loop .loop
.end:
	pop di
	pop si
	ret
